void task3_1() {
    int n;

    // використовуємо do/while для інпуту та валідації кількості елементів послідовності
    do {
        printf("Введіть кількість елементів послідовності n (n > 0): ");
        std::cin >> n;

        // валідація
        if (n <= 0) {
            printf("Помилка! Кількість має бути більшою за нуль.\n");
        }
    } while (n <= 0);

    int current_val;
    int min_val;
    int min_index = 1;

    // окремо беремо значення першого елементу 
    printf("Введіть елемент 1: ");
    std::cin >> min_val;

    // цикл для обробки наступних елементів послідовності
    for (int i = 2; i <= n; i++) {
        printf("Введіть елемент %d: ", i);
        std::cin >> current_val;

        // якщо елемнт менше мінімального то робимо той елемент мінімальним та вказуємо його індекс
        if (current_val < min_val) {
            min_val = current_val;
            min_index = i;
        }
    }

    // вивід результатів
    printf("\nМінімальний елемент: %d\n", min_val);
    printf("Номер мінімального елемента: %d\n", min_index);
}

