// винесені функції для обрахунків по заданим варіантом формулам
double f(double x) { return tan(x) - 4 * pow(x, 3); }
double f1(double z) { return 1.0 / pow(cos(z), 2); }
double f2(double z) { return z - log(std::abs(z)); }
double f3(double z) { return tan(z + 1.0 / z); }


void task2_1() {
    double x;
    double y;

    // c = 1.5 згідно варіанту 8
    const double c = 1.5;
    const double z = cos(c);

    // вибираємо потрібну функцію для обчислення X в залежності від значення Z
    if (z < 0) {
        x = f1(z);
    }
    else if (z >= 0 && z <= 8) {
        x = f2(z);
    }
    else if (z > 8) {
        x = f3(z);
    }

    // обрахунок y за формулою f(x)
    y = f(x);

    printf("Для c = %f маємо z = %f\n", c, z);
    printf("Обчислене x = %f\n", x);
    printf("Результат y = f(x) = %f\n", y);
}


