void task2_2() {
    int choice;
    double value, a, b, h, S;

    // вивід меню
    printf("Елементи трикутника:\n");
    printf("1 - катет a\n");
    printf("2 - гіпотенуза b\n");
    printf("3 - висота h\n");
    printf("4 - площа S\n");

    // ввід для вибору в меню
    printf("Введіть номер відомого елемента (1-4): ");
    std::cin >> choice;

    // ввід значення для вибраного в меню елемента
    printf("Введіть його значення: ");
    std::cin >> value;

    // перевірка, щоб значення не було від'ємним або нулем
    if (value <= 0) {
        printf("Значення елемента трикутника має бути більше нуля!\n");
        return;
    }

    switch (choice) {
    case 1: // обробка коли відомий катет a
        a = value;
        b = a * sqrt(2.0);
        h = a / sqrt(2.0);
        S = pow(a, 2) / 2.0;
        break;
    case 2: // обробка коли відома гіпотенуза b
        b = value;
        a = b / sqrt(2.0);
        h = b / 2.0;
        S = pow(b, 2) / 4.0;
        break;
    case 3: // обробка коли відома висота h
        h = value;
        a = h * sqrt(2.0);
        b = 2.0 * h;
        S = pow(h, 2);
        break;
    case 4: // обробка коли відома площа S
        S = value;
        a = sqrt(2.0 * S);
        b = 2.0 * sqrt(S);
        h = sqrt(S);
        break;
    default: // якщо не ввели з переліку
        printf("Помилка: Невідомий номер елемента!\n");
        return;
    }

    // вивід результатів
    printf("\n--- Результати обчислень ---\n");
    printf("Катет a      = %.4f\n", a);
    printf("Гіпотенуза b = %.4f\n", b);
    printf("Висота h     = %.4f\n", h);
    printf("Площа S      = %.4f\n", S);
}


