void task3_2() {
    double a = 1.0;
    double b = 4.0;
    double h = 0.2; // або ж C в таблиці до завдання

    printf("-------------------------\n");
    printf("|    x     |    F(x)    |\n");
    printf("-------------------------\n");

    double x = a; // беремо перше число початку діапазону

    // цикл while для побудови таблиці
    // додаємо h / 2.0 до межі b, щоб уникнути втрати останнього кроку
    while (x <= b + h / 2.0) {
        double F = sin(1.0 / x) + 2.0;

        // виведення рядка таблиці
        // %8.4f - де виділити 8 позицій, з них 4 після коми
        printf("| %8.4f | %10.4f |\n", x, F);
        x += h;
    }
    printf("-------------------------\n");
}

